package class6;

public class E6SwitchStatement {
    public static void main(String[] args) {

        String name="Vlad";

        switch (name){
            case "Vlad":
                System.out.println("Algonquin");

            case "Jacob":
                System.out.println("Chicago");

            case "Axel":
                System.out.println("Va");
        }
    }
}
