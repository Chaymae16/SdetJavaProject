package class8;

public class E9NestedLoop {
    public static void main(String[] args) {

        for (int i = 0; i < 100; i++) {
            for (int a = 0; a < 5; a++) {//5=how many * in the line
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
